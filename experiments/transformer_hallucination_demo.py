import math
import random

print("=" * 60)
print("实验 1：Transformer Attention —— 模式匹配，不是数据库查询")
print("=" * 60)

# 模拟 attention：一段上下文中，不同词之间的"关注度"
context = [
    "用户", "想", "把", "1000", "USDC", "换成", "ETH",
    "当前", "1", "ETH", "=", "3500", "USDC", "。",
    "但", "用户", "余额", "只有", "50", "USDC", "。"
]

# 当模型要生成下一个 token 时，attention 分布（模拟）
# 数字越高 = 模型越"关注"该位置
query = "可以换吗？"

print(f"\n上下文: {' '.join(context)}")
print(f"模型要回答: \"{query}\"")
print(f"\n模拟 Attention 权重（模型在生成时对各位置的关注度）:")
print("-" * 55)

# 模拟：attention 对价格信息（"3500 USDC"）权重高、
# 但对余额限制（"只有 50 USDC"）权重偏低
attention_weights = [
    0.02, 0.01, 0.01, 0.15, 0.08, 0.05, 0.12,  # 前半段
    0.03, 0.18, 0.20, 0.02, 0.18, 0.05, 0.00,  # 价格区域权重高
    0.00, 0.00, 0.05, 0.06, 0.03, 0.03, 0.01,  # 余额限制权重低！
]

for word, weight in zip(context, attention_weights):
    bar = "█" * int(weight * 50)
    label = "⚠️ " if weight < 0.04 and any(kw in word for kw in ["只有", "余额"]) else "   "
    print(f"  {label} '{word}'  {bar} {weight:.2f}")

print(f"""
💡 关键事实：
  Transformer 的 Attention 机制让模型在生成时"关注"上下文不同位置。
  但它不是精确检索 —— 而是概率性分配注意力。

  在这个例子里，模型高度关注 "1000 USDC" 和 "3500"（价格信息），
  但对 "余额只有 50 USDC" 关注度很低。

  结果：模型可能生成 "你可以换大约 0.28 ETH" ——
  这个答案看起来合理，但忽略了余额限制，是错的。

  🔑 Attention 擅找模式，不擅强制约束。
  🔑 余额校验这种事，必须放在规则引擎里，不能依赖 attention。
""")


print("\n" + "=" * 60)
print("实验 2：Hallucination —— 幻觉结构分析")
print("=" * 60)

print("""
幻觉不是"模型在胡说八道"，而是"模型在概率空间里生成了最合理的错误答案"。

三种典型幻觉及 Builder 应对：

┌────────────────────┬──────────────────────────────┬──────────────────────────┐
│ 幻觉类型           │ 示例                         │ Builder 应对              │
├────────────────────┼──────────────────────────────┼──────────────────────────┤
│ 编造 API / 参数    │ "调用 withdraw(amount, to,   │ schema 校验 +            │
│                    │  gas=21000)" —— gas 不是      │ 函数签名白名单            │
│                    │  withdraw 的参数              │                          │
├────────────────────┼──────────────────────────────┼──────────────────────────┤
│ 事实错误           │ "Uniswap V4 于 2024 年上线"   │ 来源引用 + 时间戳        │
│                    │ —— 实际是 2025                │                          │
├────────────────────┼──────────────────────────────┼──────────────────────────┤
│ 过时信息           │ "使用 OpenZeppelin 4.x 的      │ 版本检查 +               │
│                    │  Ownable" —— 最新是 5.x        │ 依赖最新版本查询          │
└────────────────────┴──────────────────────────────┴──────────────────────────┘

在 Agent 系统里，幻觉从"答案不好看"变成"执行风险"：
  错误参数 → 交易失败 / 资金损失
  错误权限判断 → 越权操作
  错误地址 → 资金转错

🔑 不应只靠 "更好的 prompt" 对抗幻觉。
🔑 必须在执行层前加：schema 校验、余额查询、模拟交易、人工确认。
""")


print("\n" + "=" * 60)
print("实验 3：Multimodal —— Builder 的真正价值")
print("=" * 60)

print("""
Multimodal 不是"让模型看图"，而是让模型读懂真实工作界面：

  ┌─────────────────────────┬──────────────────────────────┐
  │ 输入类型                │ Builder 场景                  │
  ├─────────────────────────┼──────────────────────────────┤
  │ 合约审计报告截图        │ 提取漏洞描述 → 生成修复方案   │
  │ Dune 仪表盘截图         │ 识别异常指标 → 生成告警       │
  │ MetaMask 确认弹窗截图   │ 解析交易参数 → 校验是否合理   │
  │ 错误堆栈截图            │ 识别错误类型 → 建议排查路径   │
  │ 设计稿 / Figma          │ 识别 UI 元素 → 生成前端代码   │
  └─────────────────────────┴──────────────────────────────┘

但多模态也有幻觉：
  - 截图里文字被遮挡 → 模型可能"脑补"
  - 图表缺少坐标 → 模型可能猜数值
  - 页面可以被伪造 → 模型无法判断截图真实性

🔑 多模态扩展了模型能"看见"的东西，但没有扩展它"验证"的能力。
🔑 关键判断仍要回到结构化数据和可信来源。

结合 Web3 的实战案例：
  用户发来 "交易成功" 截图 → 模型说 "好的已完成" → 但链上根本没这笔交易
  → 必须要求 tx hash + 链上验证，不接受截图作为唯一证据。
""")


print("\n" + "=" * 60)
print("综合总结：三层风险防线")
print("=" * 60)

print("""
  Layer 1: LLM（推理 + 生成）
    ├─ Transformer → 在上下文里找模式，但不保证正确
    ├─ Hallucination → 概率性输出，不保证事实
    └─ Multimodal → 扩展感知，不扩展验证

  Layer 2: 外部校验（确定性）
    ├─ Schema 校验 → 参数合法吗？
    ├─ 链上查询 → 余额够吗？地址对吗？
    ├─ 模拟交易 → 会成功吗？gas 够吗？
    └─ 来源引用 → 信息来自哪？什么时候的？

  Layer 3: 人工确认（最终裁决）
    ├─ 金额 > 阈值 → 必须确认
    ├─ 新合约 / 新地址 → 必须确认
    └─ 高权限操作 → 必须确认

  记住：LLM 是推理入口，不是最终验证。
  越靠近执行层，越需要确定性校验。
""")
